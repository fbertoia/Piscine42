int		ft_putchar(char c);

void	ft_print_comb(void)
{
	int i;

	i = 0;
	while (i < 790)
	{
		if ((i % 10) > (i % 100 / 10) && (i % 100 / 10) > (i / 100))
		{
			ft_putchar(48 + (i / 100));
			ft_putchar(48 + (i % 100 / 10));
			ft_putchar(48 + (i % 10));
			if (i != 789)
			{
				ft_putchar(',');
				ft_putchar(' ');
			}
		}
		i++;
	}
}
